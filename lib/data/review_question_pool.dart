  if (exact != null) return exact;

  // 找同學科最接近的年級
  int? bestGrade;
  int bestDist = 999;
  for (final k in kReviewPool.keys) {
    final parts = k.split('-');
    if (parts.length != 2) continue;
    final sid = parts[0];
    final g = int.tryParse(parts[1]) ?? -1;
    if (sid == subjectId) {
      final d = (g - grade).abs();
      if (d < bestDist) {
        bestDist = d;
        bestGrade = g;
      }
    }
  }
  if (bestGrade != null) {
    return kReviewPool[_key(subjectId, bestGrade)] ?? const [];
  }
  return const [];
}
