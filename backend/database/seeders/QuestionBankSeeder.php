    /** 挑 count 個選項，包含正解，其他用 step 隨機 */
    protected function choiceOptions(int $answer, int $step = 1, int $count = 4): array
    {
        $options = [$answer];
        $candidates = [
            $answer - $step * 2, $answer - $step, $answer + $step,
            $answer + $step * 2, $answer + $step * 3,
        ];
        $candidates = array_values(array_unique(array_filter(
            $candidates,
            fn($n) => $n !== $answer && $n >= 0
        )));
        shuffle($candidates);
        foreach ($candidates as $c) {
            if (count($options) >= $count) break;
            $options[] = $c;
        }
        while (count($options) < $count) {
            $options[] = $answer + count($options);
        }
        shuffle($options);
        return $options;
    }

