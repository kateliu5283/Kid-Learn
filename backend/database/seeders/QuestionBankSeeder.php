    protected function subject(string $code): Subject
    {
        return Subject::where('code', $code)->firstOrFail();
    }

    protected function createBatch(string $subjectCode, int $grade, array $items, string $codePrefix): void
    {
        $subject = $this->subject($subjectCode);
        foreach ($items as $i => $it) {
            Question::updateOrCreate(
                ['code' => sprintf('%s-%03d', $codePrefix, $i + 1)],
                [
                    'subject_id' => $subject->id,
                    'grade' => $grade,
                    'type' => 'multiple_choice',
                    'difficulty' => $it['difficulty'] ?? 'normal',
                    'prompt' => $it['prompt'],
                    'options' => $it['options'],
                    'correct_index' => $it['correct_index'],
                    'explanation' => $it['explanation'] ?? null,
                    'is_published' => true,
                    'is_premium' => false,
                    'sort' => $i,
                ]
            );
        }
    }

